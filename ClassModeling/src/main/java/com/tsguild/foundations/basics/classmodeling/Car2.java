package com.tsguild.foundations.basics.classmodeling;

/**
 *
 * @author bruceb
 */
public class Car2 {
    private int yearMade;
    private String carMake;
    private String carModel;
    private double carPrice;
    private String carColor;
    private int topSpeed;
    private double lengthOfCar;
    private double widthOfCar;
    private double heightOfCar;
    private double rateOfAcceleration;
    private double rateOfDeceleration;
    private int currentSpeed;
    
    

    public int getYearMade() {
        return yearMade;
    }

    public void setYearMade(int yearMade) {
        this.yearMade = yearMade;
    }

    public String getCarMake() {
        return carMake;
    }

    public void setCarMake(String carMake) {
        this.carMake = carMake;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public double getCarPrice() {
        return carPrice;
    }

    public void setCarPrice(double carPrice) {
        this.carPrice = carPrice;
    }

    public String getCarColor() {
        return carColor;
    }

    public void setCarColor(String carColor) {
        this.carColor = carColor;
    }

    public int getTopSpeed() {
        return topSpeed;
    }

    public void setTopSpeed(int topSpeed) {
        this.topSpeed = topSpeed;
    }

    public double getLengthOfCar() {
        return lengthOfCar;
    }

    public void setLengthOfCar(double lengthOfCar) {
        this.lengthOfCar = lengthOfCar;
    }

    public double getWidthOfCar() {
        return widthOfCar;
    }

    public void setWidthOfCar(double widthOfCar) {
        this.widthOfCar = widthOfCar;
    }

    public double getHeightOfCar() {
        return heightOfCar;
    }

    public void setHeightOfCar(double heightOfCar) {
        this.heightOfCar = heightOfCar;
    }

    public double getRateOfAcceleration() {
        return rateOfAcceleration;
    }

    public void setRateOfAcceleration(double rateOfAcceleration) {
        this.rateOfAcceleration = rateOfAcceleration;
    }

    public double getRateOfDeceleration() {
        return rateOfDeceleration;
    }

    public void setRateOfDeceleration(double rateOfDeceleration) {
        this.rateOfDeceleration = rateOfDeceleration;
    }

    public int getCurrentSpeed() {
        return currentSpeed;
    }

    public void setCurrentSpeed(int currentSpeed) {
        this.currentSpeed = currentSpeed;
    }
}
