package com.tsguild.foundations.basics.classmodeling;

/**
 *
 * @author Bruce_Breslin
 */
public class Airplane2 {
    private String planeMake;
    private String planeModel;
    private double topSpeed;
    private double accelerationRate;
    private double decelerationRate;
    private double weightOfPlane;
    private double heightOfPlane;
    private double lengthOfPlane;
    private double wingspanOfPlane;
    private double currentSpeed;
    private double currentAltitude;

    public String getPlaneMake() {
        return planeMake;
    }

    public void setPlaneMake(String planeMake) {
        this.planeMake = planeMake;
    }

    public String getPlaneModel() {
        return planeModel;
    }

    public void setPlaneModel(String planeModel) {
        this.planeModel = planeModel;
    }

    public double getTopSpeed() {
        return topSpeed;
    }

    public void setTopSpeed(double topSpeed) {
        this.topSpeed = topSpeed;
    }

    public double getAccelerationRate() {
        return accelerationRate;
    }

    public void setAccelerationRate(double accelerationRate) {
        this.accelerationRate = accelerationRate;
    }

    public double getDecelerationRate() {
        return decelerationRate;
    }

    public void setDecelerationRate(double decelerationRate) {
        this.decelerationRate = decelerationRate;
    }

    public double getWeightOfPlane() {
        return weightOfPlane;
    }

    public void setWeightOfPlane(double weightOfPlane) {
        this.weightOfPlane = weightOfPlane;
    }

    public double getHeightOfPlane() {
        return heightOfPlane;
    }

    public void setHeightOfPlane(double heightOfPlane) {
        this.heightOfPlane = heightOfPlane;
    }

    public double getLengthOfPlane() {
        return lengthOfPlane;
    }

    public void setLengthOfPlane(double lengthOfPlane) {
        this.lengthOfPlane = lengthOfPlane;
    }

    public double getWingspanOfPlane() {
        return wingspanOfPlane;
    }

    public void setWingspanOfPlane(double wingspanOfPlane) {
        this.wingspanOfPlane = wingspanOfPlane;
    }

    public double getCurrentSpeed() {
        return currentSpeed;
    }

    public void setCurrentSpeed(double currentSpeed) {
        this.currentSpeed = currentSpeed;
    }

    public double getCurrentAltitude() {
        return currentAltitude;
    }

    public void setCurrentAltitude(double currentAltitude) {
        this.currentAltitude = currentAltitude;
    }
   
}
